package Replit_4_Loops;

public class alphabet {
    public static void main(String[] args) {
        char ch;
        for (ch = 'A'; ch <='Z' ; ch++) {
            System.out.print((""+ch));
        }
        System.out.println();
        for (ch = 'z'; ch >='a' ; ch--) {
            System.out.print(ch);

        }
    }
}
