package Replit_4_Loops;

import java.util.Scanner;

public class hashtags {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = 0; i <n ; i++) {
            for (int j = 0; j <1 ; j++) {
                System.out.print("#");
                scan.close();
            }

        }
    }

}
